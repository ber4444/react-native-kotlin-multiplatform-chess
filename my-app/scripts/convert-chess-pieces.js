// Converts the 13 Android vector-drawable XMLs (chess pieces + no_winner icon) from
// compose-multiplatform-chess into a single TypeScript module of react-native-svg
// path descriptors. Run with: node scripts/convert-chess-pieces.js
//
// Source: compose-multiplatform-chess/app/src/commonMain/composeResources/drawable/*.xml
// Output: src/components/chess/piece-paths.generated.ts
//
// The conversion is mechanical: each <path> becomes { d, fill, stroke, strokeWidth,
// strokeLinejoin, strokeLinecap, fillRule }. Colors are preserved verbatim; the
// Android transparent sentinel #00000000 maps to 'none'.

const fs = require('fs');
const path = require('path');

const DRAWABLE_DIR = path.resolve(
  process.env.COMPOSE_CHESS_PATH || path.resolve(__dirname, '../../../compose-multiplatform-chess'),
  'app/src/commonMain/composeResources/drawable',
);
const OUT_FILE = path.resolve(__dirname, '../src/components/chess/piece-paths.generated.ts');

function convertColor(androidColor) {
  if (!androidColor || androidColor === '#00000000') return 'none';
  return androidColor;
}

function lowerOrNull(v) {
  return v ? String(v).toLowerCase() : undefined;
}

function parseVectorXml(xml) {
  const viewportW = /viewportWidth="([^"]+)"/.exec(xml)?.[1] || '45';
  const viewportH = /viewportHeight="([^"]+)"/.exec(xml)?.[1] || '45';
  const pathRegex = /<path\b([^/]*?)\/>/gs;
  const attrRegex = /android:(\w+)="([^"]*)"/g;
  const paths = [];
  let m;
  while ((m = pathRegex.exec(xml)) !== null) {
    const attrs = {};
    let am;
    while ((am = attrRegex.exec(m[1])) !== null) {
      attrs[am[1]] = am[2];
    }
    paths.push({
      d: attrs.pathData,
      fill: convertColor(attrs.fillColor),
      stroke: attrs.strokeColor || 'none',
      strokeWidth: parseFloat(attrs.strokeWidth || '1'),
      strokeLinejoin: lowerOrNull(attrs.strokeLineJoin),
      strokeLinecap: lowerOrNull(attrs.strokeLineCap),
      fillRule: attrs.fillType === 'evenOdd' ? 'evenodd' : 'nonzero',
    });
  }
  return { viewportW: parseFloat(viewportW), viewportH: parseFloat(viewportH), paths };
}

function toTl(obj, indent = '    ') {
  const lines = [];
  for (const p of obj.paths) {
    const parts = [`      d: ${JSON.stringify(p.d)}`, `      fill: ${JSON.stringify(p.fill)}`];
    if (p.stroke && p.stroke !== 'none') parts.push(`      stroke: ${JSON.stringify(p.stroke)}`);
    if (p.strokeWidth) parts.push(`      strokeWidth: ${p.strokeWidth}`);
    if (p.strokeLinejoin) parts.push(`      strokeLinejoin: ${JSON.stringify(p.strokeLinejoin)}`);
    if (p.strokeLinecap) parts.push(`      strokeLinecap: ${JSON.stringify(p.strokeLinecap)}`);
    if (p.fillRule && p.fillRule !== 'nonzero') parts.push(`      fillRule: ${JSON.stringify(p.fillRule)}`);
    lines.push(`    {\n${parts.join(',\n')},\n    }`);
  }
  return lines.join(',\n');
}

const files = fs.readdirSync(DRAWABLE_DIR).filter(f => f.endsWith('.xml')).sort();
const pieces = {};
const viewports = {};
for (const f of files) {
  const key = f.replace(/\.xml$/, '');
  const xml = fs.readFileSync(path.join(DRAWABLE_DIR, f), 'utf8');
  const parsed = parseVectorXml(xml);
  pieces[key] = parsed.paths;
  viewports[key] = { w: parsed.viewportW, h: parsed.viewportH };
}

const banner = `// AUTO-GENERATED by scripts/convert-chess-pieces.js — DO NOT EDIT.
// Source: compose-multiplatform-chess/.../composeResources/drawable/*.xml
// Each entry is an array of react-native-svg path descriptors.
//
// Keys: <piece>_<light|dark> for the 12 piece drawables, plus 'no_winner'.

export interface PiecePath {
  d: string;
  fill: string;
  stroke?: string;
  strokeWidth?: number;
  strokeLinejoin?: 'miter' | 'round' | 'bevel';
  strokeLinecap?: 'butt' | 'round' | 'square';
  fillRule?: 'evenodd' | 'nonzero';
}

export const PIECE_VIEWBOX = 45;

`;

const piecesStr = Object.entries(pieces).map(([k, paths]) => {
  const ps = paths.map(p => {
    const parts = [`      d: ${JSON.stringify(p.d)}`, `      fill: ${JSON.stringify(p.fill)}`];
    if (p.stroke && p.stroke !== 'none') parts.push(`      stroke: ${JSON.stringify(p.stroke)}`);
    parts.push(`      strokeWidth: ${p.strokeWidth}`);
    if (p.strokeLinejoin) parts.push(`      strokeLinejoin: ${JSON.stringify(p.strokeLinejoin)} as const`);
    if (p.strokeLinecap) parts.push(`      strokeLinecap: ${JSON.stringify(p.strokeLinecap)} as const`);
    if (p.fillRule && p.fillRule !== 'nonzero') parts.push(`      fillRule: ${JSON.stringify(p.fillRule)} as const`);
    return `    {\n${parts.join(',\n')},\n    }`;
  }).join(',\n');
  return `  ${JSON.stringify(k)}: [\n${ps},\n  ] as PiecePath[],`;
}).join('\n');

const output = banner + `export const PIECE_PATHS: Record<string, PiecePath[]> = {\n${piecesStr}\n};\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, output);
console.log(`Wrote ${Object.keys(pieces).length} piece paths to ${path.relative(process.cwd(), OUT_FILE)}`);
